let a = 10;
let b = 20;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
// Tricky
console.log(a + b + "hi");
console.log("hi" + a + b);

console.log("20" - "10");
console.log("20" * "10");
console.log("20" / "2");
console.log("20" % 2);

console.log("30" / "hi");

// Comparision Operators
let c = "20";
// Literal syntax
console.log(b == c);
console.log(b === c);

let d = new Number(20); // object syntax
console.log(b === d);
console.log(b == d);
// == compares values 
// === compares types and values
